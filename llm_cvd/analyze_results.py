"""Analyze API RAG evaluation results by provider/model."""

from __future__ import annotations

import argparse
import json
from pathlib import Path

import pandas as pd
from sklearn.metrics import accuracy_score, f1_score, precision_score, recall_score


SUMMARY_FIELDS = [
    "provider",
    "model",
    "n",
    "accuracy",
    "precision",
    "recall",
    "f1",
    "avg_latency_sec",
    "avg_input_tokens",
    "avg_output_tokens",
    "avg_total_tokens",
    "estimated_cost_usd",
    "error_count",
]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("results_path", help="JSONL or CSV generated by run_rag_api_eval.py")
    parser.add_argument("--pricing", default="pricing_config.json")
    parser.add_argument("--output", default=None)
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    df = read_results(args.results_path)
    pricing = load_pricing(args.pricing)
    summary = summarize(df, pricing)

    if args.output:
        output_path = Path(args.output)
        output_path.parent.mkdir(parents=True, exist_ok=True)
        summary.to_csv(output_path, index=False)
    print(summary.to_string(index=False))


def read_results(path: str) -> pd.DataFrame:
    result_path = Path(path)
    if result_path.suffix.lower() == ".jsonl":
        return pd.read_json(result_path, lines=True)
    return pd.read_csv(result_path)


def load_pricing(path: str) -> dict[str, dict[str, float]]:
    pricing_path = Path(path)
    if not pricing_path.exists():
        return {}
    with pricing_path.open("r", encoding="utf-8") as handle:
        return json.load(handle)


def summarize(df: pd.DataFrame, pricing: dict[str, dict[str, float]]) -> pd.DataFrame:
    if df.empty:
        return pd.DataFrame(columns=SUMMARY_FIELDS)

    error_mask = df["error"].notna() & (df["error"].astype(str) != "")
    error_counts = (
        df[error_mask]
        .groupby(["provider", "model"], dropna=False)
        .size()
        .rename("error_count")
    )
    valid = df[~error_mask].copy()
    rows = []

    for (provider, model), group in valid.groupby(["provider", "model"], dropna=False):
        y_true = group["true_label"].astype(str)
        y_pred = group["pred_label"].fillna("").astype(str)
        price = pricing.get(str(model), pricing.get(str(provider), {}))
        rows.append(
            {
                "provider": provider,
                "model": model,
                "n": len(group),
                "accuracy": accuracy_score(y_true, y_pred),
                "precision": precision_score(y_true, y_pred, pos_label="Vulnerable", zero_division=0),
                "recall": recall_score(y_true, y_pred, pos_label="Vulnerable", zero_division=0),
                "f1": f1_score(y_true, y_pred, pos_label="Vulnerable", zero_division=0),
                "avg_latency_sec": pd.to_numeric(group["latency_sec"], errors="coerce").mean(),
                "avg_input_tokens": pd.to_numeric(group["input_tokens"], errors="coerce").mean(),
                "avg_output_tokens": pd.to_numeric(group["output_tokens"], errors="coerce").mean(),
                "avg_total_tokens": pd.to_numeric(group["total_tokens"], errors="coerce").mean(),
                "estimated_cost_usd": estimate_cost(group, price),
                "error_count": int(error_counts.get((provider, model), 0)),
            }
        )

    return pd.DataFrame(rows, columns=SUMMARY_FIELDS).sort_values(["provider", "model"])


def estimate_cost(group: pd.DataFrame, price: dict[str, float]) -> float | None:
    if not price:
        return None
    input_per_1m = price.get("input_per_1m")
    output_per_1m = price.get("output_per_1m")
    if input_per_1m is None or output_per_1m is None:
        return None
    input_tokens = pd.to_numeric(group["input_tokens"], errors="coerce").fillna(0).sum()
    output_tokens = pd.to_numeric(group["output_tokens"], errors="coerce").fillna(0).sum()
    return (input_tokens / 1_000_000 * input_per_1m) + (output_tokens / 1_000_000 * output_per_1m)


if __name__ == "__main__":
    main()
