"""Analyze API evaluation results by provider/model and repeated run."""

from __future__ import annotations

import argparse
import json
from pathlib import Path

import pandas as pd
from sklearn.metrics import accuracy_score, f1_score, precision_score, recall_score


SUMMARY_FIELDS = [
    "provider",
    "model",
    "k",
    "repeat_count",
    "n",
    "n_min",
    "n_max",
    "accuracy",
    "accuracy_std",
    "precision",
    "precision_std",
    "recall",
    "recall_std",
    "f1",
    "f1_std",
    "avg_tp",
    "avg_fp",
    "avg_tn",
    "avg_fn",
    "total_tp",
    "total_fp",
    "total_tn",
    "total_fn",
    "avg_latency_sec",
    "avg_input_tokens",
    "avg_output_tokens",
    "avg_total_tokens",
    "avg_estimated_cost_usd",
    "total_estimated_cost_usd",
    "error_count",
]

PER_REPEAT_FIELDS = [
    "provider",
    "model",
    "k",
    "repeat_id",
    "n",
    "accuracy",
    "precision",
    "recall",
    "f1",
    "tp",
    "fp",
    "tn",
    "fn",
    "avg_latency_sec",
    "avg_input_tokens",
    "avg_output_tokens",
    "avg_total_tokens",
    "estimated_cost_usd",
    "error_count",
]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("results_path", help="JSONL or CSV generated by an API evaluation runner.")
    parser.add_argument("--pricing", default="pricing_config.json")
    parser.add_argument("--output", default=None)
    parser.add_argument(
        "--per-repeat-output",
        default=None,
        help="Optional CSV path for metrics calculated separately for each repeat_id.",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    df = read_results(args.results_path)
    pricing = load_pricing(args.pricing)
    per_repeat = summarize_per_repeat(df, pricing)
    summary = summarize_repeat_means(per_repeat)

    if args.output:
        output_path = Path(args.output)
        output_path.parent.mkdir(parents=True, exist_ok=True)
        summary.to_csv(output_path, index=False)
    if args.per_repeat_output:
        per_repeat_output_path = Path(args.per_repeat_output)
        per_repeat_output_path.parent.mkdir(parents=True, exist_ok=True)
        per_repeat.to_csv(per_repeat_output_path, index=False)
    print(summary.to_string(index=False))


def read_results(path: str) -> pd.DataFrame:
    result_path = Path(path)
    if not result_path.exists():
        raise FileNotFoundError(f"Results file does not exist: {result_path}")
    if result_path.suffix.lower() == ".jsonl":
        df = pd.read_json(str(result_path), lines=True)
    else:
        df = pd.read_csv(result_path)
    if "repeat_id" not in df.columns:
        df["repeat_id"] = 0
    if "k" not in df.columns:
        df["k"] = 0
    return df


def load_pricing(path: str) -> dict[str, dict[str, float]]:
    pricing_path = Path(path)
    if not pricing_path.exists():
        return {}
    with pricing_path.open("r", encoding="utf-8") as handle:
        return json.load(handle)


def summarize_per_repeat(df: pd.DataFrame, pricing: dict[str, dict[str, float]]) -> pd.DataFrame:
    if df.empty:
        return pd.DataFrame(columns=PER_REPEAT_FIELDS)

    error_mask = df["error"].notna() & (df["error"].astype(str) != "")
    df = df.copy()
    df["_has_error"] = error_mask
    rows = []

    for (provider, model, k_value, repeat_id), group in df.groupby(
        ["provider", "model", "k", "repeat_id"], dropna=False
    ):
        valid_group = group[~group["_has_error"]]
        price = pricing.get(str(model), pricing.get(str(provider), {}))
        error_count = int(group["_has_error"].sum())
        if valid_group.empty:
            metric_values = {
                "accuracy": None,
                "precision": None,
                "recall": None,
                "f1": None,
                "tp": 0,
                "fp": 0,
                "tn": 0,
                "fn": 0,
            }
        else:
            y_true = valid_group["true_label"].astype(str)
            y_pred = valid_group["pred_label"].fillna("").astype(str)
            tp, fp, tn, fn = confusion_counts(y_true, y_pred)
            metric_values = {
                "accuracy": accuracy_score(y_true, y_pred),
                "precision": precision_score(y_true, y_pred, pos_label="Vulnerable", zero_division=0),
                "recall": recall_score(y_true, y_pred, pos_label="Vulnerable", zero_division=0),
                "f1": f1_score(y_true, y_pred, pos_label="Vulnerable", zero_division=0),
                "tp": tp,
                "fp": fp,
                "tn": tn,
                "fn": fn,
            }
        rows.append(
            {
                "provider": provider,
                "model": model,
                "k": k_value,
                "repeat_id": repeat_id,
                "n": len(valid_group),
                **metric_values,
                "avg_latency_sec": pd.to_numeric(valid_group["latency_sec"], errors="coerce").mean(),
                "avg_input_tokens": pd.to_numeric(valid_group["input_tokens"], errors="coerce").mean(),
                "avg_output_tokens": pd.to_numeric(valid_group["output_tokens"], errors="coerce").mean(),
                "avg_total_tokens": pd.to_numeric(valid_group["total_tokens"], errors="coerce").mean(),
                "estimated_cost_usd": estimate_cost(valid_group, price),
                "error_count": error_count,
            }
        )

    return pd.DataFrame(rows, columns=PER_REPEAT_FIELDS).sort_values(["provider", "model", "k", "repeat_id"])


def summarize_repeat_means(per_repeat: pd.DataFrame) -> pd.DataFrame:
    if per_repeat.empty:
        return pd.DataFrame(columns=SUMMARY_FIELDS)

    rows = []
    for (provider, model, k_value), group in per_repeat.groupby(["provider", "model", "k"], dropna=False):
        estimated_costs = pd.to_numeric(group["estimated_cost_usd"], errors="coerce")
        rows.append(
            {
                "provider": provider,
                "model": model,
                "k": k_value,
                "repeat_count": group["repeat_id"].nunique(),
                "n": group["n"].mean(),
                "n_min": group["n"].min(),
                "n_max": group["n"].max(),
                "accuracy": group["accuracy"].mean(),
                "accuracy_std": group["accuracy"].std(ddof=0),
                "precision": group["precision"].mean(),
                "precision_std": group["precision"].std(ddof=0),
                "recall": group["recall"].mean(),
                "recall_std": group["recall"].std(ddof=0),
                "f1": group["f1"].mean(),
                "f1_std": group["f1"].std(ddof=0),
                "avg_tp": group["tp"].mean(),
                "avg_fp": group["fp"].mean(),
                "avg_tn": group["tn"].mean(),
                "avg_fn": group["fn"].mean(),
                "total_tp": group["tp"].sum(),
                "total_fp": group["fp"].sum(),
                "total_tn": group["tn"].sum(),
                "total_fn": group["fn"].sum(),
                "avg_latency_sec": group["avg_latency_sec"].mean(),
                "avg_input_tokens": group["avg_input_tokens"].mean(),
                "avg_output_tokens": group["avg_output_tokens"].mean(),
                "avg_total_tokens": group["avg_total_tokens"].mean(),
                "avg_estimated_cost_usd": estimated_costs.mean(),
                "total_estimated_cost_usd": estimated_costs.sum() if estimated_costs.notna().any() else None,
                "error_count": group["error_count"].sum(),
            }
        )

    return pd.DataFrame(rows, columns=SUMMARY_FIELDS).sort_values(["provider", "model", "k"])


def confusion_counts(y_true: pd.Series, y_pred: pd.Series) -> tuple[int, int, int, int]:
    true_vulnerable = y_true == "Vulnerable"
    pred_vulnerable = y_pred == "Vulnerable"
    true_safe = y_true == "Safe"
    pred_safe = y_pred == "Safe"
    tp = int((true_vulnerable & pred_vulnerable).sum())
    fp = int((true_safe & pred_vulnerable).sum())
    tn = int((true_safe & pred_safe).sum())
    fn = int((true_vulnerable & pred_safe).sum())
    return tp, fp, tn, fn


def estimate_cost(group: pd.DataFrame, price: dict[str, float]) -> float | None:
    if not price:
        return None
    input_per_1m = price.get("input_per_1m")
    output_per_1m = price.get("output_per_1m")
    if input_per_1m is None or output_per_1m is None:
        return None
    input_tokens = pd.to_numeric(group["input_tokens"], errors="coerce").fillna(0).sum()
    output_tokens = pd.to_numeric(group["output_tokens"], errors="coerce").fillna(0).sum()
    return (input_tokens / 1_000_000 * input_per_1m) + (output_tokens / 1_000_000 * output_per_1m)


if __name__ == "__main__":
    main()
