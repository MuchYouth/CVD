def load_full_code_maps(target_dataset_csv: Path, rag_dataset_root: Path) -> dict[str, dict[str, str]]:
    from llm_cvd.data.juliet_loader import load_juliet_csv_train_records, load_real_vul_records

    query_records = load_real_vul_records(target_dataset_csv)
    retrieved_records = load_juliet_csv_train_records(rag_dataset_root)
    return {
        "queries": {str(row["sample_id"]): str(row["code"]) for row in query_records},
        "retrieved": {str(row["sample_id"]): str(row["code"]) for row in retrieved_records},
    }


def load_embedding_tokenizer(embedding_model_name: str):
    try:
        from transformers import AutoTokenizer, logging
    except ModuleNotFoundError as exc:
        raise ModuleNotFoundError(
            "Grouped full-code HTML with token view requires transformers. "
            "Run with the project virtualenv, for example ../.venv/bin/python."
        ) from exc
    logging.set_verbosity_error()
    return AutoTokenizer.from_pretrained(embedding_model_name, local_files_only=True)


def token_info(
    tokenizer,
    code: str,
    max_rag_tokens: int,
    truncation_side: str,
    cache: dict[str, dict[str, str | bool | int]],
    include_token_list: bool,
) -> dict[str, str | bool | int]:
    cache_key = f"{max_rag_tokens}\0{truncation_side}\0{code}"
    if cache_key in cache:
        return cache[cache_key]

    token_count = len(tokenizer.tokenize(code)) + tokenizer.num_special_tokens_to_add(pair=False)
    old_truncation_side = tokenizer.truncation_side
    tokenizer.truncation_side = truncation_side
    try:
        rag_ids = tokenizer.encode(
            code,
            add_special_tokens=True,
            truncation=True,
            max_length=max_rag_tokens,
        )
    finally:
        tokenizer.truncation_side = old_truncation_side
    truncated = token_count > max_rag_tokens
    kept_side = "tail" if truncation_side == "left" else "head"
    info = {
        "total_tokens": token_count,
        "rag_tokens": len(rag_ids),
        "max_rag_tokens": max_rag_tokens,
        "kept_side": kept_side,
        "truncated": truncated,
        "decoded": tokenizer.decode(rag_ids, skip_special_tokens=True) if truncated else "",
    }
    if include_token_list:
        info["tokens"] = " ".join(tokenizer.convert_ids_to_tokens(rag_ids))
    cache[cache_key] = info
    return info


def write_token_box(
    handle,
    title: str,
    info: dict[str, str | bool | int],
    include_token_list: bool,
) -> None:
    truncated_text = "yes" if info["truncated"] else "no"
    handle.write(
        "<div class=\"token-box\">\n"
        f"<div class=\"token-meta\"><strong>{html.escape(title)}</strong>: "
        f"{info['rag_tokens']} / {info['total_tokens']} tokens used "
        f"(max {info['max_rag_tokens']}, kept: {info['kept_side']}, "
        f"truncated: {truncated_text})</div>\n"
    )
    if info["truncated"]:
        handle.write(
            "<details>\n"
            "<summary>Show truncated CodeBERT input text</summary>\n"
            f"<pre class=\"token-text\">{html.escape(str(info['decoded']))}</pre>\n"
            "</details>\n"
        )
    else:
        handle.write("<div class=\"token-meta\">Not truncated; RAG input is the full code shown above.</div>\n")
    if include_token_list:
        handle.write(
            "<details>\n"
            "<summary>Show CodeBERT tokens</summary>\n"
            f"<pre class=\"token-list\">{html.escape(str(info.get('tokens', '')))}</pre>\n"
            "</details>\n"
        )
    handle.write("</div>\n")


def write_select(handle, field: str, value: str) -> None:
    handle.write(f"<select data-field=\"{html.escape(field)}\">")
    for option in ["", "yes", "partial", "no"]:
        selected = " selected" if value == option else ""
        label = option if option else "-"
        handle.write(f"<option value=\"{option}\"{selected}>{label}</option>")
    handle.write("</select>")


def write_cell(handle, field: str, value: str) -> None:
    escaped = html.escape(value)
    if field in {"cause_match", "context_match", "helpful_as_fewshot"}:
        write_select(handle, field, value)
        return
    if field == "note":
        handle.write(f"<textarea data-field=\"{html.escape(field)}\">{escaped}</textarea>")
        return
    handle.write(f"<span data-field=\"{html.escape(field)}\">{escaped}</span>")


def column_class(field: str) -> str:
    if field in {"query_snippet", "retrieved_snippet"}:
        return "snippet"
    if field == "faiss_l2_distance":
        return "distance"
    if field == "note":
        return "note"
    if field in {"query_sample_id", "rank", "cause_match", "context_match", "helpful_as_fewshot"}:
        return "narrow"
    return ""


