"""Prepare a compact CSV for qualitative review of retrieved RAG examples.

The output intentionally excludes label-match columns so reviewers can focus on
whether retrieved traces share the same vulnerability cause and code context as
the queried CVE trace.
"""

from __future__ import annotations

import argparse
import csv
import html
import sys
from collections import defaultdict
from pathlib import Path


PROJECT_ROOT = Path(__file__).resolve().parents[2]
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))


REVIEW_FIELDS = [
    "query_sample_id",
    "query_project",
    "rank",
    "faiss_l2_distance",
    "query_snippet",
    "retrieved_snippet",
    "cause_match",
    "context_match",
    "helpful_as_fewshot",
    "note",
]

DEFAULT_INPUT = "results/rag_retrieval_inspection_cve_k6.csv"
DEFAULT_OUTPUT = "results/rag_retrieval_qualitative_review_cve_k6.csv"
DEFAULT_GROUPED_HTML = "results/rag_retrieval_qualitative_review_cve_k6_grouped_full.html"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--input-csv",
        default=DEFAULT_INPUT,
        help="Inspection CSV generated by inspect_rag_retrieval.py.",
    )
    parser.add_argument(
        "--output-csv",
        default=DEFAULT_OUTPUT,
        help="Compact qualitative review CSV to write.",
    )
    parser.add_argument(
        "--overwrite-notes",
        action="store_true",
        help="Clear existing manual review notes when reformatting a review CSV.",
    )
    parser.add_argument(
        "--output-html",
        default=None,
        help="Optional HTML table with wrapped snippet/note columns for easier reading.",
    )
    parser.add_argument(
        "--output-grouped-html",
        default=None,
        help="Optional grouped HTML review page with full query/retrieved code.",
    )
    parser.add_argument(
        "--target-dataset-csv",
        default="../dataset/cve_Real_Vul_data.csv",
        help="Target CVE CSV used to recover full query code for grouped HTML.",
    )
    parser.add_argument(
        "--rag-dataset-root",
        default="../dataset/juliet_Real_Vul_data.csv",
        help="RAG train CSV used to recover full retrieved code for grouped HTML.",
    )
    parser.add_argument(
        "--embedding-model-name",
        default="microsoft/codebert-base",
        help="Tokenizer name used by the RAG embedding model.",
    )
    parser.add_argument(
        "--max-rag-tokens",
        type=int,
        default=512,
        help="Maximum CodeBERT tokens used by the RAG embedding path.",
    )
    parser.add_argument(
        "--embedding-truncation-side",
        choices=("left", "right"),
        default="left",
        help="Tokenizer truncation side used by the RAG embedding path. 'left' keeps tail tokens.",
    )
    parser.add_argument(
        "--include-token-list",
        action="store_true",
        help="Include raw CodeBERT token lists in grouped HTML. This makes the file much heavier.",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    input_path = Path(args.input_csv)
    output_path = Path(args.output_csv)

    if not input_path.exists():
        raise FileNotFoundError(f"Input CSV does not exist: {input_path}")

    rows = read_review_rows(input_path, overwrite_notes=args.overwrite_notes)
    if not rows:
        raise ValueError(f"Input CSV has no rows: {input_path}")

    output_path.parent.mkdir(parents=True, exist_ok=True)
    with output_path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=REVIEW_FIELDS)
        writer.writeheader()
        writer.writerows({field: row.get(field, "") for field in REVIEW_FIELDS} for row in rows)

    if args.output_html:
        write_html_review(Path(args.output_html), rows)
    if args.output_grouped_html:
        write_grouped_full_html(
            Path(args.output_grouped_html),
            rows,
            target_dataset_csv=Path(args.target_dataset_csv),
            rag_dataset_root=Path(args.rag_dataset_root),
            embedding_model_name=args.embedding_model_name,
            max_rag_tokens=args.max_rag_tokens,
            embedding_truncation_side=args.embedding_truncation_side,
            include_token_list=args.include_token_list,
        )

    query_count = len({row["query_sample_id"] for row in rows})
    print(f"Wrote {len(rows)} rows for {query_count} CVE queries to {output_path}")
    if args.output_html:
        print(f"Wrote wrapped HTML review table to {args.output_html}")
    if args.output_grouped_html:
        print(f"Wrote grouped full-code HTML review page to {args.output_grouped_html}")
    print("Manual columns: cause_match, context_match, helpful_as_fewshot, note")
    print("Suggested values: yes / partial / no")


def read_review_rows(input_path: Path, overwrite_notes: bool) -> list[dict[str, str]]:
    with input_path.open(newline="", encoding="utf-8") as handle:
        reader = csv.DictReader(handle)
        validate_columns(reader.fieldnames or [])
        rows = []
        for row in reader:
            rows.append(
                {
                    "query_sample_id": row.get("query_sample_id", ""),
                    "query_project": row.get("query_project", ""),
                    "rank": row.get("rank", ""),
                    "faiss_l2_distance": row.get("faiss_l2_distance", ""),
                    "query_snippet": row.get("query_snippet", ""),
                    "retrieved_sample_id": row.get("retrieved_sample_id", ""),
                    "retrieved_snippet": row.get("retrieved_snippet", ""),
                    "cause_match": note_value(row, "cause_match", overwrite_notes),
                    "context_match": note_value(row, "context_match", overwrite_notes),
                    "helpful_as_fewshot": note_value(row, "helpful_as_fewshot", overwrite_notes),
                    "note": note_value(row, "note", overwrite_notes),
                }
            )
    return rows


def validate_columns(fieldnames: list[str]) -> None:
    required = {
        "query_sample_id",
        "query_project",
        "rank",
        "faiss_l2_distance",
        "query_snippet",
        "retrieved_snippet",
    }
    missing = sorted(required - set(fieldnames))
    if missing:
        raise ValueError(f"Input CSV is missing required columns: {', '.join(missing)}")


def note_value(row: dict[str, str], field: str, overwrite_notes: bool) -> str:
    if overwrite_notes:
        return ""
    return row.get(field, "")


