def write_contact_sheet(path: Path, panel_infos: list[dict[str, Any]], output_dir: Path) -> None:
    from PIL import Image, ImageDraw

    images = []
    for info in panel_infos:
        image = Image.open(output_dir / info["image_name"]).convert("RGB")
        image.thumbnail((420, 320))
        images.append((info, image.copy()))
        image.close()

    cols = 4
    cell_w = 450
    cell_h = 365
    rows = int(np.ceil(len(images) / cols))
    sheet = Image.new("RGB", (cols * cell_w, rows * cell_h), "white")
    draw = ImageDraw.Draw(sheet)
    for index, (info, image) in enumerate(images):
        col = index % cols
        row = index // cols
        x = col * cell_w + 15
        y = row * cell_h + 30
        title = f"Q{info['query_id']} {info['query_record'].get('project', '')}"
        draw.text((x, row * cell_h + 8), title[:58], fill=(31, 41, 55))
        sheet.paste(image, (x, y))
    path.parent.mkdir(parents=True, exist_ok=True)
    sheet.save(path)


